# Function that given a name and location, prints some messages to terminal :

def greet(name, location) :

    print(f"Hello {name}.\n")
    print(f"How do you do, {name}? \n")
    print(f"What is the weather like in {location} ? \n")

# Calls function given coded in inputs :

greet("Angela", "Edinburgh")
